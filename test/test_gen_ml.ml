(** Tests for {!Fpp.Gen_ml}: FPP to OCaml code generation.

    Each test shows the exact generated OCaml for a given FPP input. The tests
    serve as living documentation of the codegen: if the output changes, the
    diff tells you exactly what happened.

    {b State machine features}: simple transitions, typed signals,
    actions/guards, choices, nested states, entry/exit actions, do-actions,
    typed actions/guards, choice chains, inherited transitions, type definitions
    (enums, structs, arrays), catch-all patterns, external (bodyless) state
    machines.

    {b Topology features}: topologies, typed ports, functor application, default
    functor convention, topology import, ocaml.type annotations, passive
    components, bound leaf instances. *)

(* ── Helpers ─────────────────────────────────────────────────────── *)

let parse s =
  match Fpp.parse_string s with
  | tu -> tu
  | exception Fpp.Parse_error e ->
      Alcotest.failf "parse error: %a" Fpp.pp_error e

let render s =
  let tu = parse s in
  let sms = Fpp.state_machines tu in
  let buf = Buffer.create 256 in
  let ppf = Format.formatter_of_buffer buf in
  List.iter (Fpp.Gen_ml.pp ppf) sms;
  Format.pp_print_flush ppf ();
  Buffer.contents buf

let render_topo s =
  let tu = parse s in
  let topos = Fpp.topologies tu in
  let buf = Buffer.create 256 in
  let ppf = Format.formatter_of_buffer buf in
  List.iter (Fpp.Gen_ml.pp_topology tu ppf) topos;
  Format.pp_print_flush ppf ();
  Buffer.contents buf

let render_topo_mli s =
  let tu = parse s in
  let topos = Fpp.topologies tu in
  let buf = Buffer.create 256 in
  let ppf = Format.formatter_of_buffer buf in
  List.iter (Fpp.Gen_ml.pp_topology_mli tu ppf) topos;
  Format.pp_print_flush ppf ();
  Buffer.contents buf

let strip_trailing_ws s =
  String.split_on_char '\n' s
  |> List.map (fun line ->
      let len = String.length line in
      let i = ref (len - 1) in
      while !i >= 0 && (line.[!i] = ' ' || line.[!i] = '\t') do
        decr i
      done;
      String.sub line 0 (!i + 1))
  |> String.concat "\n"

let check_output ~msg actual expected =
  Alcotest.(check string)
    msg
    (strip_trailing_ws (String.trim expected))
    (strip_trailing_ws (String.trim actual))

