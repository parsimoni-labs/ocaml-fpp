let test_choice_chain () =
  check_output ~msg:"choices targeting choices need let rec"
    (render
       {|
    state machine M {
      guard g1
      guard g2
      signal s
      initial enter S1
      state S1 { on s enter C1 }
      state S2
      state S3
      choice C1 { if g1 enter S2 else enter C2 }
      choice C2 { if g2 enter S3 else enter S1 }
    } |})
    {|(* Generated by ofpp to-ml from state machine M *)
type s1
type s2
type s3

type _ state =
  | S1 : s1 state
  | S2 : s2 state
  | S3 : s3 state

type any = State : _ state -> any

type signal =
  | S

module type GUARDS = sig
  type ctx
  val g1 : ctx -> bool
  val g2 : ctx -> bool
end

module Make (G : GUARDS) : sig
  type t
  val create : G.ctx -> t
  val state : t -> any
  val step : t -> signal -> t
end = struct
  type t = { state : any; ctx : G.ctx }

  let state t = t.state

  let rec enter_c1 t =
    if G.g1 t.ctx then (
      { t with state = State S2 })
    else (
      enter_c2 t)

  and enter_c2 t =
    if G.g2 t.ctx then (
      { t with state = State S3 })
    else (
      { t with state = State S1 })

  let step t signal =
    match t.state, signal with
    | State S1, S ->
        enter_c1 t
    | _ -> t

  let create ctx =
    { state = State S1; ctx }
end|}

