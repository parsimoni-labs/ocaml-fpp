type signal =
  | S

module type ACTIONS = sig
  type ctx
  val a1 : ctx -> unit
end

module type GUARDS = sig
  type ctx
  val g : ctx -> bool
end

module Make (A : ACTIONS) (G : GUARDS with type ctx = A.ctx) : sig
  type t
  val create : A.ctx -> t
  val state : t -> any
  val step : t -> signal -> t
end = struct
  type t = { state : any; ctx : A.ctx }

  let state t = t.state

  let enter_c t =
    if G.g t.ctx then (
      A.a1 t.ctx;
      { t with state = State S })
    else (
      { t with state = State S })

  let step t signal =
    match t.state, signal with
    | _ -> t

  let create ctx =
    let t = { state = State S; ctx } in
    enter_c t
end|}

