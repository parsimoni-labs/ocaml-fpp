  | S1 : s1 state
  | S2 : s2 state
  | S3 : s3 state

type any = State : _ state -> any

type signal =
  | S

module type GUARDS = sig
  type ctx
  val g1 : ctx -> bool
  val g2 : ctx -> bool
end

module Make (G : GUARDS) : sig
  type t
  val create : G.ctx -> t
  val state : t -> any
  val step : t -> signal -> t
end = struct
  type t = { state : any; ctx : G.ctx }

  let state t = t.state

  let rec enter_c1 t =
    if G.g1 t.ctx then (
      { t with state = State S2 })
    else (
      enter_c2 t)

  and enter_c2 t =
    if G.g2 t.ctx then (
      { t with state = State S3 })
    else (
      { t with state = State S1 })

  let step t signal =
    match t.state, signal with
    | State S1, S ->
          enter_c1 t
    | _ -> t

  let create ctx =
    { state = State S1; ctx }
end|}

