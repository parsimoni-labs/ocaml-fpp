end

module type GUARDS = sig
  type ctx
  val g : ctx -> bool
end

module Make (A : ACTIONS) (G : GUARDS with type ctx = A.ctx) : sig
  type t
  val create : A.ctx -> t
  val state : t -> any
  val step : t -> signal -> t
