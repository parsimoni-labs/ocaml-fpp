module Make (A : ACTIONS) (G : GUARDS with type ctx = A.ctx) : sig
  type t
  val create : A.ctx -> t
  val state : t -> any
  val step : t -> signal -> t
end = struct
  type t = { state : any; ctx : A.ctx }

  let state t = t.state

  let step t signal =
    match t.state, signal with
    | State Closed, Open ->
        if G.locked t.ctx then begin
          { t with state = State Closed }
